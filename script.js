const btn = document.getElementById("generate-password-btn");
const password1 = document.getElementById("password1");
const password2 = document.getElementById("password2");

btn.addEventListener("click", function () {
  let password = [];
  for (let j = 0; j < 2; j++) {
    for (let i = 0; i < 15; i++) {
      let passwordChar = Math.floor(Math.random() * 91);
      password.push(characters[passwordChar]);
    }
  }
  let password1Content = password.slice(0, 15).join("");
  let password2Content = password.slice(15, 30).join("");
  password1.textContent = password1Content;
  password2.textContent = password2Content;
});
